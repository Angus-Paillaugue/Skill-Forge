<script>
	import { enhance } from '$app/forms';
	import { newToast } from '$lib/stores';
	import { Undo2 } from 'lucide-svelte';
	import { Spinner, Input } from '$lib/components';

	const { data, form } = $props();
	const { user } = data;

	let isSavingInfos = $state(false);
	let isChangingPassword = $state(false);
	let updatedUser = $state(user);
	let hasChangedInfos = $state(false);

	$effect(() => {
		hasChangedInfos = Object.entries(updatedUser).some(([k, v]) => v !== user[k]);
	});

	$effect(() => {
		if (form?.error) {
			newToast({ type: 'red', title: 'Error', message: form.error, timeout: 2000 });
		} else if (form?.success) {
			newToast({ type: 'green', title: 'Success', message: form.success, timeout: 2000 });
		}
	});
</script>

<svelte:head>
	<title>Settings</title>
</svelte:head>

<main class="mx-auto flex w-full max-w-screen-lg flex-col gap-10 p-4 sm:px-6 sm:py-0 md:gap-8">
	<div class="rounded-lg bg-neutral-800 p-4 text-neutral-100">
		<div class="flex flex-row gap-4">
			<a href="/app/account" class="rounded-full bg-neutral-900 p-2" arial-label="Go back"
				><Undo2 class="size-5" /></a
			>
			<h1 class="text-3xl font-bold">Settings</h1>
		</div>
	</div>
	<div class="rounded-lg bg-neutral-800 p-4 text-neutral-100">
		<form
			method="POST"
			action="?/saveInfos"
			class="flex flex-col gap-2"
			use:enhance={() => {
				isSavingInfos = true;
				return async ({ update }) => {
					await update();
					isSavingInfos = false;
				};
			}}
		>
			<h2 class="text-2xl font-semibold">Personal info</h2>
			<Input
				id="username"
				label="Username"
				bind:value={updatedUser.username}
				placeholder="Username"
				class="h-12"
			/>

			<button
				class="mt-4 flex w-full flex-row items-center justify-center gap-2 rounded-xl bg-neutral-700 p-2 text-base font-semibold transition-all disabled:cursor-not-allowed disabled:opacity-50"
				disabled={!hasChangedInfos}
			>
				{#if isSavingInfos}
					<Spinner class="size-5" />
				{/if}
				Save
			</button>
		</form>
	</div>
	<div class="rounded-lg bg-neutral-800 p-4 text-neutral-100">
		<form
			method="POST"
			action="?/changePassword"
			class="flex flex-col gap-2"
			use:enhance={() => {
				isChangingPassword = true;
				return async ({ update }) => {
					await update();
					isChangingPassword = false;
				};
			}}
		>
			<h2 class="text-2xl font-semibold">Change password</h2>
			<Input
				id="oldPassword"
				label="Current Password"
				placeholder="Current Password"
				type="password"
				class="h-12"
			/>
			<div class="h-2"></div>
			<Input
				id="newPassword"
				label="New Password"
				placeholder="New Password"
				type="password"
				class="h-12"
			/>
			<Input
				id="confirmPassword"
				label="Confirm password"
				placeholder="Confirm password"
				type="password"
				class="h-12"
			/>

			<button
				class="mt-4 flex w-full flex-row items-center justify-center gap-2 rounded-xl bg-neutral-700 p-2 text-base font-semibold"
			>
				{#if isChangingPassword}
					<Spinner class="size-5" />
				{/if}
				Change
			</button>
		</form>
	</div>
</main>
